package com.mycompany.kafka;

public class Student {
    // Data members (also instance variables)
    private int id;
    private String name;

    // Constructors
    public Student() {
        // Default constructor
    }

    public Student(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
